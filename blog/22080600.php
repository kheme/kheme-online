<b>* </b>Honey is the only food that doesn&#39;t spoil<br />
<b>* </b>In Shakespeare&#39;s time, mattresses were secured on bed 
frames by ropes. When you pulled on the ropes the mattress 
tightened, making the bed firmer to sleep on. Hence the phrase 
&quot;goodnight, sleep tight.&quot;<br />
<b>* </b>It was the accepted practice in Babylon 4,000 years ago 
that for a month after the wedding, the bride&#39;s father would 
supply his son-in-law with all the mead he could drink. Mead is 
a honey beer and because their calendar was lunar based, this 
period was called the honey month, which we know today as the 
&quot;honeymoon&quot;.<br />
<b>* </b>In English pubs, ale is ordered by pints and quarts... 
So in old England, when customers got unruly, the bartender 
would yell at them, &quot;Mind your pints and quarts, and settle 
down&quot; Hence the phrase &quot;mind your P&#39;s and Q&#39;s&quot;<br />
<b>* </b>Many years ago in England, pub frequenters had a 
whistle baked into the rim, or handle, of their ceramic cups. 
When they needed a refill, they used the whistle to get some 
service. &quot;Wet your whistle&quot; is the phrase inspired by this 
practice. </p>
