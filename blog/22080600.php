* </b>If a statue in the park of a person on a horse has both 
front legs in the air, the person died in battle. If the horse 
has one front leg in the air the person died as a result of 
wounds received in battle. If the horse has all four legs on the 
ground, the person died of natural causes.<br />
<b>* </b>Half of all Americans live within 50 miles of their 
birthplace.<br />
<b>* </b>If you were to spell out numbers, you would have to go 
until One thousand to find the letter &quot;A&quot;<br />
<b>* </b>Bulletproof vests, fire escapes, windshield wipers, and 
