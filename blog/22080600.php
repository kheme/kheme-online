<b>* </b>The original name for butterfly was flutterby.<br />
<b>* </b>The phrase &quot;rule of thumb&quot; is derived from an old 
English law which stated that you couldn&#39;t beat your wife with 
anything wider than your thumb.<br />
<b>* </b>The first product Motorola started to develop was a 
record player for automobiles. At that time, the most known 
player on the market was Victrola, so they called themselves 
