<p>I don&#39;t know if I&#39;m in a good mood to write this morning; 
it&#39;s 11:01AM on a Friday morning and my dad&#39;s phone call just 
woke me up about 20 minutes ago and now, here I am... writing!</p>
<p>Okay, I think I&#39;ve gotten it! Perhaps, I can say I understand what&#39;s 
up with the break up thingy. Listen to this...</p>
<p>A friend of mine right now, was in love with a guy last semester. 
Now, this guy only wanted her as &quot;a friend&quot; but she wanted to be his 
girlfriend. She wanted him to much, he didn&#39;t want her at all and she 
would cry everyday just because she could see him and not have him! She 
said it made her life miserable, just seeing him and &quot;just&quot; talking with 
him but nothing more than that and she wanted much more than that with 
this guy!</p>
<p>One day I called her and she was crying on the phone and I wondered 
why. She said she saw this guy &quot;talking&quot; with some other girl, so she 
burst into tears (the guy didn&#39;t see her)! She felt so bitter, knowing 
she can&#39;t have this guy she&#39;s always wanted and she told me she was 
going to revenge and cause him to feel the same pains!</p>
<p>Sometimes, just mentioning this guys name would cause her to GO MAD 
at me and start shouting! When I asked why, she said... you know! I 
don&#39;t even want to hear his name cos I&#39;ll start thinking again and I 
can&#39;t bear the pain any longer... just stop it, i don&#39;t want to think of 
him!</p>
<p>So its clear now... the reason they decide to seize all communication 
is just the avoid the &quot;pain&quot; they go through, perhaps, just knowing that 
it didn&#39;t work, after putting so much into the relationship! And the 
only way the &quot;think&quot; they can deal with it is to stop all communication 
and interaction with the &quot;ex-lover&quot; so they can think less and be 
happier.</p>
<p>I just think they don&#39;t know how to handle the situation best! I&#39;ve 
had &quot;thoughts&quot; like these, but I never actually did a thing like that... 
I think its childish and premature!</p>
