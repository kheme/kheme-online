<p>I&#39;ve always heard that cats have 9 lives, but like every 
&quot;story&quot; I hear, I&#39;m always indifferent, I&#39;m always 50% for and 
50% against, waiting for the slightest evidence to convince me 
and increase my percentage or doubt or believe. So for cats 
having 9 lives, i was 50% for and 50% against!</p><p>Anyway, here&#39;s 
my story...<br />
I just took my bath and had lunch on Sunday evening (Sept. 3rd 2006), I 
was waiting so I could leave for Church when I heard a cat screaming 
outside. I went out of the apartment and traced the cat to a window on 
the other side of the bathroom window, on the other side of the 
corridor.</p><p>
<img border="0" src="../smalls/animal5.jpg" width="160" height="107" align="left" />I 
thought the cat looked cute, but it was acting very weird and fierce 
when I approached it, so I approached it with caution! But it was a 
little cat, a kitten but quite big, so i didn&#39;t mind cos I didn&#39;t think 
it could harm me.</p><p>I held the cat and it was screaming loudly and 
scratching the air, so I let it go! I returned to the apartment and told 
the guys about it and my friend mentioned that it almost got into the 
bathroom through the window while he was taking his hath... so he 
quickly slammed the window against the cat.</p><p>I went back and the cat 
was virtually &quot;barking&quot; at me, lol. So I let it be! I slid the windows 
locking it out, then suddenly, the cat jumped at the window, slammed the 
glass and fell down! Mind you, my apartment is on the 5th floor... so 
this cat fell down FIVE (5) floors and landed on CONCRETE FLOOR!!! I 
stood there, looking at the still cat on the floor, feeling a bit 
guilty. I came back a minute later, and it was still there! wow, I just 
killed a cat... or something like that.</p><p>
<img border="0" src="../smalls/animal00.jpg" width="160" height="107" align="right" />I 
was feeling a bit bad, so I went inside and told the guys and they were 
like... that cat was a stupid cat! Minutes later, I went back and I saw 
this cat walking about on the ground floor! But this time, it was 
silent. I made some noise and it looked up at me, and continued to walk 
about on the floor.</p><p>Initially when i saw the cat still and looking 
dead, I remembered the phrase &quot;a cat has 9 lives&quot; and then I laughed in 
my mind and said &quot;ha, 9 lives my a**&quot;. But when I saw the cat moving 
about, I rethought things again. And in fact, it was the cats scream 
that reminded me to write this again, over 4 hours since the cat fell!</p><p>
So tell me, what do you think??? Do cats really have 9 lives or what??? 
And of course, if the cat was smashed by a moving vehicle, there&#39;s NO 
WAY that cat&#39;s coming back... lol!</p><p>Your comments are welcome.<br /></p>
