<p>The future will always remain the same, even if you knew a part of 
it!</p><p>What got me thinking was a day I was with my friend and he was 
fooling around, saying he can predict my future within the next couple 
of minutes! Well, I&#39;m a critical thinker, and I&#39;ve always know that the 
future is CONSTANT... it never changes!</p><p>So I laughed and asked, what 
em I gonna do next? And he said by 2PM, I&#39;d leave and go home; I laughed 
and looked at the clock, it was 5 minutes to 2PM. Then a few seconds 
later, he said &quot;no no! Since I told you you&#39;d leave by 2PM, you&#39;d decide 
to stay!&quot; And YES, I had decided to stay! Then he spoke again &quot;Now I&#39;ve 
mentioned this, you would decide to go, again&quot;. And NO, I didn&#39;t decide 
to go!</p><p>But I played the whole thing in my head, and what he would have 
said <i>if he really knew my future</i>, would go something like this... 
&quot;we would have a little debate about when you&#39;d leave, then you&#39;d change 
your mind a couple of times about when to leave or if you&#39;d leave just 
yet; then eventually after 23 minutes, we&#39;ll both forget about the 
discussion cos of the scrabble we were playing, then you&#39;d receive a 
call from your dad by 2:45PM, and leave my house at exactly 3PM&quot;.</p><p>Now, 
if he said something like that, I might have believed! lol. But my point 
here is... even if you know the future, there&#39;s little or nothing you 
can do about it, to change or alter it! Or can you??? If you had a FULL 
picture of the future, you&#39;d see yourself <i>trying</i> to alter the 
part of the future you now know, so that it might not happen as it 
should have; but you will also see the <i>outcome</i> of your efforts in 
trying to alter the future, right?</p><p>If at all you know the future, I 
don&#39;t think its really for you to alter it, but to accept it and be 
ready to face it... what do you think?</p><p>Your comments are welcome.<br /></p>
