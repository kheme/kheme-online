<p>Hello there! welcome to my first blog, which is 
more like a dialogue about blogs. Just read... and make whatever 
comments you want!</p>
<p>The very first time I heard about blogs was 
in, I think 2002 or so, when I just had my first free Tripod 
account (I don&#39;t even know if Tripod still has free accounts cos 
the last time I visited, I got lost finding my way to the signup 
page!).</p>
<p>Then, I got an invitation from Tripod, telling me about their 
new &quot;blog&quot; thing! They said a blog was something more like a 
personal journal or diary, online; I could write stories about 
myself, about my pet, friends, about my daily experiences, etc.</p>
<p>But the part I really picked was about &quot;daily experiences&quot;; 
like where you went, what you did there, how your day went 
generally, etc. So... I liked the idea initially and I made a 
few entries; how I saw this new blog thing and had to write how 
it was and what I thought about it. Then I made another entry at 
some other time and I think that was my last entry! lol!</p>
<p>Well, the reason I&#39;m writing now is because I SEE BLOGS 
EVERYWHERE NOW!!! I&#39;m a member of a forum for Nigeria (niaraland) 
and I&#39;ve seen 1 too many links to people&#39;s personal blogs and 
stuff. So, I&#39;m like... WHAT&#39;S UP WITH THE BLOG THING??? cos it 
seems like a &quot;lucrative&quot; business right now!</p>
<p>Anyway, one very good use of blogs that i know, is that a 
good content can generate you some money if you have Google 
AdSense running with your blogs. Then again, if you&#39;re having 
difficulty making a commitment to SIT YOUR ASS DOWN and write a 
book, then a blog, of course, is a lazy man&#39;s approach to doing 
that!</p>
<p>Okay, i have to end this page here before it gets too too 
long! To read about the &quot;<a href="30080601.php"><font color="#808000">lazy 
man&#39;s approach to writing a book</font></a>&quot;, just click here 
and read all about it on a new blog entry!
