<p><b>1.
First Important Lesson - Cleaning Lady.</b><br />
During my second month of college, our professor gave us a pop 
quiz. I was a conscientious student and had breezed through the 
questions until I read the last one: &quot;What is the first name of 
the woman who cleans the school?&quot; Surely this was some kind of 
joke. I had seen the cleaning woman several times. She was tall, 
dark-haired and in her 50s, but how would I know her name? I 
handed in my paper, leaving the last question blank. Just before 
class ended, one student asked if the last question would count 
toward our quiz grade. &quot;Absolutely,&quot; said the professor. &quot;In 
your careers, you will meet many people. All are significant. 
They deserve your attention and care, even if all you do is 
smile and say &quot;hello&quot;. I&#39;ve never forgotten that lesson. I also 
learned her name was Dorothy.
</p>
<p>
<b>2.&nbsp;Second Important Lesson - Serve others!<br />
</b>Pickup in the Rain One night, at 11.30 p.m., an 
older African American woman was standing on the side of an Alabama 
highway trying to endure a lashing rainstorm. Her car had broken down 
and she desperately needed a ride. Soaking wet, she decided to flag down 
the next car. A young white man stopped to help her, generally unheard 
of in those conflict-filled 1960s. The man took her to safety, helped 
her get assistance and put her into a taxicab. She seemed to be in a big 
hurry, but wrote down his address and thanked him. Seven days went by 
and a knock came on the man&#39;s door. To his surprise, a giant console 
color TV was delivered to his home. A special note was attached. It 
read: &quot;Thank you so much for assisting me on the highway the other 
night. The rain drenched not only my clothes, but also my spirits. Then 
you came along. Because of you, I was able to make it to my dying 
husband&#39;s bedside just before he passed away. God bless you for helping 
me and unselfishly serving others.&quot; Sincerely, Mrs. Nat King Cole.</p>
<p>
<b>3. Third Important Lesson - Always remember those who serve!<br />
</b>In the days when an ice cream sundae cost much 
less, a 10 year-old boy entered a hotel coffee shop and sat at a table. 
A waitress put a glass of water in front of him. &quot;How much is an 
ice-cream sundae?&quot; he asked. &quot;Fifty cents,&quot; replied the waitress. The 
little boy pulled is hand out of his pocket and studied the coins in it. 
&quot;Well, how much is a plain dish of ice cream?&quot; he inquired. By now more 
people were waiting for a table and the waitress was growing impatient.</p>
<p>&quot;Thirty-five cents,&quot; she brusquely replied. The 
little boy again counted his coins. &quot;I&#39;ll have the plain ice cream,&quot; he 
said. The waitress brought the ice cream, put the bill on the table and 
walked away. The boy finished the ice cream, paid the cashier and left. 
When the waitress came back, she began to cry as she wiped down the 
table. There, placed neatly beside the empty dish, were two nickels and 
five pennies.. You see, he couldn&#39;t have the sundae, because he had to 
have enough left to leave her a tip.</p>
<p>
<b>4. Fourth Important Lesson. - The obstacle in Our Path!<br />
</b>In ancient times, a King had a boulder placed on a 
roadway. Then he hid himself and watched to see if anyone would remove 
the huge rock. Some of the king&#39;s wealthiest merchants and courtiers 
came by and simply walked around it. Many loudly blamed the King for not 
keeping the roads clear, but none did anything about getting the stone 
out of the way. Then a peasant came along carrying a load of vegetables. 
Upon approaching the boulder, the peasant laid down his burden and tried 
to move the stone to the side of the road. After much pushing and 
straining, he finally succeeded. After the peasant picked up his load of 
vegetables, he noticed a purse lying in the road where the boulder had 
been. The purse contained many gold coins and a note from the King 
indicating that the gold was for the person who removed the boulder from 
the roadway. The peasant learned what many of us never understand! Every 
obstacle presents an opportunity to improve our condition.</p>
<p>
<b>5. Fifth Important Lesson - Giving when it Counts!</b><br />
Many years ago, when I worked as a volunteer at a hospital, I got to 
know a little girl named Liz who was suffering from a rare &amp; serious 
disease. Her only chance of recovery appeared to be a blood transfusion 
from her 5-year old brother, who had miraculously survived the same 
disease and had developed the antibodies needed to combat the illness. 
The doctor explained the situation to her little brother, and asked the 
little boy if he would be willing to give his blood to his sister. I saw 
him hesitate for only a moment before taking a deep breath and saying, 
&quot;Yes I&#39;ll do it if it will save her.&quot; As the transfusion progressed, he 
lay in bed next to his sister and smiled, as we all did, seeing the 
color returning to her cheek. Then his face grew pale and his smile 
faded. He looked up at the doctor and asked with a trembling voice, &quot;Will I start to die right away&quot;. Being young, the little boy had 
misunderstood the doctor; he thought he was going to have to give his 
sister all of his blood in order to save her.</p><p>Your comments are welcome.<br /></p>
