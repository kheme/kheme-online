<p>Do you feel.. em.. LAZY to make a commitment to dedicate time 
to writing a book?? cos at times, i feel so so lazy, just 
thinking of it... I have to SIT DOWN and carry a pen and paper 
(or my laptop) and write and write and write and the thought 
alone makes me tired already! ha! Do you ever feel that way?<br />
<br />
There&#39;s good news though! This is not an original idea of mine, 
but I find it interesting and it works as well! It still require 
some level of commitment though, but quite little!</p>
<p>If you&#39;re that lazy, then why not BLOG IT??? Yea, BLOG IT! Its quite 
simple... startup a blog somewhere (there are many free blogs on the 
internet, pick on) and write SOMETHING about a top (in this case, the 
title of your book) and keep adding new entries everyday or there about.</p>
<p>At the end of the day, you&#39;d get to spend about 30 to 45 to 65 
minutes writing SOMETHING about your book in your blog! Now, you can 
keep this up for a while... say 2, 3 or 5 months and before you know it, 
you&#39;ve got more than enough to make up your COMPLETE book! yea! That 
easy and it really works!</p>
<p>Here&#39;s my story... I&#39;m a website developer and people often ask me 
for materials and I&#39;ve got none! So I decided to write my own book on 
website development! But, I was.. em.. LAZY!</p>
<p>But anyway, I decided to start up with my HTML tutorial website, 
where I intended to drop one HTML lesson per week, so people will have 
time THROUGHOUT the week for my HTML lessons!</p>
<p>But I felt so eager to write. Instead of one HTML lesson a week, I 
did one topic a day, and I completed the site sooner than I thought! 
Here&#39;s the website now,
<a target="_blank" href="http://www.htmlonline.tk">
<font color="#808000">www.htmlonline.tk</font></a>.</p>
<p>After I had the site uploaded, I realized (a few days later) that I 
had ALREADY COMPLETED MY EBOOK!!! wow! I couldn&#39;t believe it! So, what I 
just did was get an eBook compiler (in this case, a .chm help file 
maker) and compiled all the HTML lessons into one piece, and I got my 
eBook... WITHOUT REALIZING THE TIME I PUT IN!</p>
<p>So? Blogging your new book will work JUST THE SAME WAY! A few minutes 
or hours daily is all it takes and after a couple of weeks or months or 
so, you just couple everything together, re-edit it and print, compile, 
or whatever you want to do with it!</p><p>For more information about
