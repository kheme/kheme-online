<p>If you had just one (1) wish, what would you wish for??? A 
million dollars? 10 million? A billion? 100 more wishes? good 
health? long life? wealth? A faithful life partner? God&#39;s 
guidance? What on earth will you wish for???</p>
<p>Well, I&#39;ve asked a couple of friends this question before and they 
give me answers... &quot;God&#39;s guidance&quot;, &quot;God&#39;s protection&quot;, &quot;God&#39;s 
protection for my family and boyfriend&quot;, &quot;love&quot;, &quot;good health&quot;, &quot;favour&quot;, 
etc.</p>
<p>But my <i>second</i> question is always to em.. find fault in 
whatever they&#39;ve wished for! My aim is this...<br />
What if you wished for so much money, then you died the next day, what&#39;s 
the use? Or, what is you wished for good health, and still, you died the 
next week? Or, what if you wished for long life and the next month, you 
were wrongfully accused and sentenced to life imprisonment; what next? 
Live long in jail? Or, what if you wished for God&#39;s guidance? And you 
know, having God&#39;s guidance DOES NOT mean that you&#39;d follow, cos I 
believe EVERYONE has God&#39;s guidance, but NOT everyone FOLLOWS! So, I ask 
again, what&#39;s the use?</p>
<p>You may wish for something, anything and most times, your &quot;choice&quot; on 
what to wish for is often influenced by your present condition! Some few 
people think of the future, but the future <i>itself</i> is NEVER 
predictable... unless of course you have a way to see into the future! 
But still, <a href="14090601.php"><font color="#808000">the future 
remains the future</font></a>! Whatever you wish for, how can you be so 
sure it&#39;s the <i>right</i> thing to wish for? Unless of course its bus 
fair for you to get to school; or a meal for lunch; or the money you owe 
a friend, due in 5 minutes; or a book for an exam in 3 days; or 
something else of that <i>nature</i>... if you know what I mean!</p>
<p>So I ask again... if you had just <i>one</i> (1) wish, what would you 
wish for???</p>
