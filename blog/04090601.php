<p>Welcome to another one of my &quot;monologues&quot; about what I call 
the &quot;Break Up&quot; ideology!</p><p>Okay, its like this... 
you&#39;re a guy, and you meet this girl you for the first time (strangers), 
then you talk, chat and get to know each other a bit more 
(acquaintances). Thereafter, you see each other once a while, talk, go 
out, etc. (friends). A couple of weeks or months later, you decided to 
take things to another level with her, then you become 
boyfriend/girlfriend (lovers) and so on and so forth! After a while, 
something (I don&#39;t know what!!!) suddenly happened and then, you both 
decide to end the &quot;relationship&quot; (enemies)!</p><p>So let me recapitulate for 
you, here&#39;s the circle of &quot;relationship between them both...</p><p style="text-align: center">
<b>strangers &gt; acquaintances &gt; friends &gt; lovers &gt; enemies</b></p><p style="text-align: left">
My question here is this...WHAT ON EARTH IS UP WITH LOVERS AUTOMATICALLY 
BECOMING ENEMIES JUST BECAUSE THEY BROKE UP AND ARE NO LONGER 
LOVERS??????</p><p style="text-align: left">I mean... the guy changes his 
number and email address; the girl can NO LONGER visit him; he puts her 
email on black list; deletes her phone number, and blocks it as well; 
avoids going to places where his likely to meet her; like she&#39;s become 
this contagious disease to be avoided... why? why? why???</p><p style="text-align: left">
It&#39;s really funny cos I still don&#39;t get the logic or reasoning behind 
this CHILDISH behaviours! Yes! it&#39;s childish, cos it&#39;s only a child that 
will act like such... &quot;never talk to me in your entire life because you 
didn&#39;t let me to your birthday party!&quot; only this time, it&#39;s something 
like... &quot;I never EVER want to have ANYTHING to do with you cos we are no 
longer lovers!&quot; The guys and the girls do it!!!!</p><p style="text-align: left">
So... perhaps, one or two of you reading this may know &quot;why&quot; people act 
like this! i see it everywhere, i read it a lot and I&#39;ve had one 
experience before!</p><p>Your comments are welcome.<br /></p>
